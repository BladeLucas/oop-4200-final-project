namespace DefaultNamespace;

public class human
{
    public Human(string race, int strength, int dexterity, int constitution, int intellegence, int wisdom, int charisma)
    {
        if (race = "human")
        {
            strength++;
            dexterity++;
            constitution++;
            intellegence++;
            wisdom++;
            charisma++;
        }
    }
    
}